# Ввод количества элементов
n = int(input("Введите количество элементов: "))

# Инициализация переменной для максимального модуля
max_mod = 0

# Цикл для обработки каждого элемента
i = 1
while i <= n:
    # Ввод очередного числа
    num = float(input(f"Введите элемент {i}: "))
    
    # Вычисление модуля
    mod = num if num >= 0 else -num
    
    # Сравнение с текущим максимумом
    if mod > max_mod:
        max_mod = mod
    
    i = i + 1

# Вывод результата
print("Максимальный из модулей элементов:", max_mod)